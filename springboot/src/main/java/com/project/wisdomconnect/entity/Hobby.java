package com.project.wisdomconnect.entity;

public class Hobby {
    private Integer id;
    private String name;
    private Integer level;
    private Integer tophobbyid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public Integer getTophobbyid() {
        return tophobbyid;
    }

    public void setTophobbyid(Integer tophobbyid) {
        this.tophobbyid = tophobbyid;
    }


}
